# Isto é usado para converter as letras em letras maiúsculas.
print("Oi Python!".upper())

# Isto é usado para converter as letras em minúsculas.
print("Oi Python!".lower())

# Isto é usado para unir dois textos
print("Oi Python!" + " Tudo bom?" + " ok")

# Isto serve para separar cada palavra de toda a expressão.
print("Oi Python! 2023".split())
