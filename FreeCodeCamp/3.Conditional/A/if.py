x = 5
if x == 5:
    print('Equals 5')
if x > 4:
    print('Greater than 4')
if x >= 5:
    print('Greater than or equals 5')
if x < 6:
    print('Less than 6')
if x <= 5:
    print('Less than or equal 5')
if x != 6:
    print('Not equal 6')

for i in range(5):
    print(i)
    if i > 2:
        print('Bigger than 2')
    print('Done with i', i)
print('All Done')
